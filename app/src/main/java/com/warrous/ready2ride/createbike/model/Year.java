package com.warrous.ready2ride.createbike.model;

public class Year {
    int YearId;
    String YearValue;
    int BrandYearId;

    public int getYearId() {
        return YearId;
    }

    public void setYearId(int yearId) {
        YearId = yearId;
    }

    public String getYearValue() {
        return YearValue;
    }

    public void setYearValue(String yearValue) {
        YearValue = yearValue;
    }

    public int getBrandYearId() {
        return BrandYearId;
    }

    public void setBrandYearId(int brandYearId) {
        BrandYearId = brandYearId;
    }
}
